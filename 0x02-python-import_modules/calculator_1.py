#!/usr/bin/python3
def add(a, b):
    """My addition sum function

    Args:
        a: first integer
        b: second integer

    Returns:
        The value return value. a + b
    """
    return (a + b)


def sub(a, b):
    """My subtraction minus function

    Args:
        a: first integer
        b: second integer

    Returns:
        The value for return. a - b
    """
    return (a - b)


def mul(a, b):
    """My multiplication x function

    Args:
        a: first integer
        b: second integer

    Returns:
        The value of return. a * b
    """
    return (a * b)


def div(a, b):
    """My division / function

    Args:
        a: first integer
        b: second integer

    Returns:
        The return value. a / b
    """
    return int(a / b)
